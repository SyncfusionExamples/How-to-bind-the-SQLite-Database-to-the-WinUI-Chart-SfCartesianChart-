namespace WinUI_ChartSqliteSample
{
    /// <summary>
    /// An empty page that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class MainPage : Page
    {
        public MainPage()
        {
            this.InitializeComponent();
            this.Loaded += MainPage_Loaded;
        }

        private void MainPage_Loaded(object sender, RoutedEventArgs e)
        {
            listView.ItemsSource = App.Database.GetChartDataModel();
        }

        private void GenerateChart_Clicked(object sender, RoutedEventArgs e)
        {
            Frame.Navigate(typeof(ChartSample));
        }

        private void Insert_Clicked(object sender, RoutedEventArgs e)
        {
            Frame.Navigate(typeof(AddChartDataPage));
        }
    }
